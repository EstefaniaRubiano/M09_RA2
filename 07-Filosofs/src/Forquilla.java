public class Forquilla {
    private int n_forquilles;
    private boolean enUs;

    public Forquilla(int n_forquilles) {
        this.n_forquilles = n_forquilles;
        this.enUs = false;
    }

    public int getN_forquilles() {
        return n_forquilles;
    }

    public void setN_forquilles(int n_forquilles) {
        this.n_forquilles = n_forquilles;
    }

    public boolean isEnUs() {
        return enUs;
    }

    public void setEnUs(boolean enUs) {
        this.enUs = enUs;
    }

    
}
